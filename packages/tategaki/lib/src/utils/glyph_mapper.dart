/// 横書き用の文字を縦書き用の字形に変換するユーティリティ
class GlyphMapper {
  GlyphMapper._();

  /// 文字を縦書き用の字形に変換する
  ///
  /// マッピングが存在しない場合はそのまま返す
  static String map(String char) {
    return _mapping[char] ?? char;
  }

  static const _mapping = <String, String>{
    // スペース
    ' ': '　',

    // 矢印
    '↑': '→',
    '↓': '←',
    '←': '↑',
    '→': '↓',

    // 句読点
    '。': '︒',
    '、': '︑',
    ',': '︐',
    '､': '︑',
    '.': '﹒', // U+002E → U+FE52 縦書き小点
    '!': '︕', // U+0021 → U+FE15 縦書き感嘆符
    '?': '︖', // U+003F → U+FE16 縦書き疑問符
    // 長音・ダッシュ
    'ー': '丨',
    '─': '丨',
    '-': '丨',
    'ｰ': '丨',
    '_': '丨',
    '−': '丨',
    '－': '丨',
    '—': '丨',
    '―': '丨', // U+2015 HORIZONTAL BAR
    '–': '丨', // U+2013 EN DASH
    '‐': '丨', // U+2010 HYPHEN
    '〜': '丨',
    '～': '丨',

    // その他記号
    '／': '＼',
    '…': '︙',
    '‥': '︰',
    '︙': '…',
    '：': '︓',
    ':': '︓',
    '；': '︔',
    ';': '︔',
    '・': '｜', // U+30FB カタカナ中点
    '･': '｜', // U+FF65 半角カタカナ中点
    '＝': '॥',
    '=': '॥',

    // 丸括弧
    '（': '︵',
    '(': '︵',
    '）': '︶',
    ')': '︶',

    // 角括弧
    '［': '﹇',
    '[': '﹇',
    '］': '﹈',
    ']': '﹈',

    // 波括弧
    '｛': '︷',
    '{': '︷',
    '｝': '︸',
    '}': '︸',

    // 山括弧
    '＜': '︿',
    '<': '︿',
    '＞': '﹀',
    '>': '﹀',

    // 鉤括弧
    '「': '﹁',
    '」': '﹂',
    '『': '﹃',
    '』': '﹄',
    '｢': '﹁',
    '｣': '﹂',

    // 隅付き括弧
    '【': '︻',
    '】': '︼',
    '〖': '︗',
    '〗': '︘',

    // 二重山括弧
    '《': '︽', // U+300A → U+FE3D 縦書き二重山括弧左
    '》': '︾', // U+300B → U+FE3E 縦書き二重山括弧右
  };
}
